# generate code to sum two integers
def sum_two_integers(a, b):
    """Return the sum of two integers or numbers.

    This function is intentionally simple so it can be imported and
    unit-tested without executing example code at import time.
    """
    return a + b


if __name__ == "__main__":
    # Example usage
    result = sum_two_integers(3, 5)
    print("The sum is:", result)  # Output: The sum is: 8